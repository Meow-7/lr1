# Дано значення кута α в градусах (0 ≤ α < 360). Обчислити значення цього ж кута в радіанах,
# враховуючи, що 180 ° = π радіанів. У якості значення π використовувати 3.14.
# Шимрило Олексій
a = int(input("Degrees = "))


def degree_to_radian(a):
    rad = (a*3.14)/180
    print("Radians = ", rad)
    return rad


degree_to_radian(a)
